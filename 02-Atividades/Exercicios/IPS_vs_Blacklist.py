def _detecao_ameaca(blacklist, requisicoes):
    
    ameacas_contadas = {}
    cont_ameaca = 0
    
    for ameaca in blacklist:
        if ameaca in requisicoes:
            cont_ameaca = requisicoes.count(ameaca)
            ameacas_contadas[ameaca] = cont_ameaca
    
    return ameacas_contadas
# ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
def _avaliador_de_amecaca(contagem_ameacas):
    for ip, requisicoes in contagem_ameacas.items():
        
        print()
        if  requisicoes > 100:
            print(f"IP malicioso: {ip}")
            print(f"Requisições: {requisicoes}")
            print("Nivel ameaça: crítico!")
        
        elif 50 < requisicoes <= 100:
            print(f"IP malicioso: {ip}")
            print(f"Requisições: {requisicoes}")
            print("Nivel ameaça: Alta!")

        elif 10 < requisicoes <= 50:
            print(f"IP malicioso: {ip}")
            print(f"Requisições: {requisicoes}")
            print("Nivel ameaça: Média!")

        else:
            print(f"IP malicioso: {ip}")
            print(f"Requisições: {requisicoes}")
            print("Nivel ameaça: Baixa!")

